/**
 * op_add - add two numbers
 * @a: first number
 * @b: second number
 * Return: addition of a and b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two numbers
 * @a: first number
 * @b: second number
 * Return: substraction of a and b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first number
 * @b: second number
 * Return: the multiplicaton of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: first number
 * @b: second number
 * Return: the division of a by b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get the remainder
 * @a: first number
 * @b: second number
 * Return: the modulo of a by b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
